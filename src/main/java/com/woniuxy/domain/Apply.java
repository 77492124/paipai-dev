public class Apply {
    private Integer apid;

    private String apno;

    private Integer apbusi;

    private String apinfo;

    private Integer apstate;

    private String apreason;

    private Integer uid;

    public Integer getApid() {
        return apid;
    }

    public void setApid(Integer apid) {
        this.apid = apid;
    }

    public String getApno() {
        return apno;
    }

    public void setApno(String apno) {
        this.apno = apno;
    }

    public Integer getApbusi() {
        return apbusi;
    }

    public void setApbusi(Integer apbusi) {
        this.apbusi = apbusi;
    }

    public String getApinfo() {
        return apinfo;
    }

    public void setApinfo(String apinfo) {
        this.apinfo = apinfo;
    }

    public Integer getApstate() {
        return apstate;
    }

    public void setApstate(Integer apstate) {
        this.apstate = apstate;
    }

    public String getApreason() {
        return apreason;
    }

    public void setApreason(String apreason) {
        this.apreason = apreason;
    }

    public Integer getUid() {
        return uid;
    }

    public void setUid(Integer uid) {
        this.uid = uid;
    }
}